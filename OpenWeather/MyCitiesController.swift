    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: "MyCitiesCell", for: indexPath) as? MyCitiesCell else {
            preconditionFailure("Error")
        }

           cell.cityName.text = cities[indexPath.row]

        return cell
    }
    
    @IBAction func addCity(_ segue: UIStoryboardSegue) {
        
        guard segue.identifier == "addCity",
              let allCittiesController = segue.source as? AllCitiesController,
              let indexPath = allCittiesController.tableView.indexPathForSelectedRow
        else {return}
        
        let city = allCittiesController.cities[indexPath.row]
      
        if !cities.contains(city) {
            cities.append(city)
            tableView.reloadData()
        }
    }
    

